export function searchLocalFoods(query: string) {
  const normalizedQuery = normalize(query);
  const expandedTerms = expandQuery(normalizedQuery);

  return localFoods
    .map((food) => ({
      food,
      score: scoreFood(food, normalizedQuery, expandedTerms),
    }))
    .filter((item) => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .map((item) => item.food);
}

export function normalizeFoodText(value: string) {
  return normalize(value);
}

function scoreFood(
  food: FoodSearchResult,
  query: string,
  expandedTerms: string[],
) {
  const name = normalize(`${food.name} ${food.brand ?? ""}`);

  if (name === query) return 100;
  if (name.startsWith(query)) return 80;
  if (name.includes(query)) return 60;

  const matchingAlias = expandedTerms.some((term) => name.includes(term));
  return matchingAlias ? 40 : 0;
}

function expandQuery(query: string) {
  const terms = new Set([query]);

  for (const values of Object.values(aliases)) {
    const normalizedAliases = values.map(normalize);

    if (normalizedAliases.some((alias) => query.includes(alias))) {
      normalizedAliases.forEach((alias) => terms.add(alias));
    }
  }

  return [...terms];
}

function normalize(value: string) {
  return value
    .toLocaleLowerCase("tr-TR")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/ı/g, "i")
    .replace(/[^a-z0-9çğıöşü\s]/gi, " ")
    .replace(/\s+/g, " ")
    .trim();
}
